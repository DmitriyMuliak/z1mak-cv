import { clsx, type ClassValue } from 'clsx';
import { twMerge } from 'tailwind-merge';

// oklch ‚Äî —Ü–µ –Ω–æ–≤—ñ—Ç–Ω—ñ–π –ø—Ä–æ—Å—Ç—ñ—Ä –∫–æ–ª—å–æ—Ä—ñ–≤, —Å—Ç–≤–æ—Ä–µ–Ω–∏–π –¥–ª—è –ª—é–¥—Å—å–∫–æ–≥–æ —Å–ø—Ä–∏–π–Ω—è—Ç—Ç—è,
// –∞ –Ω–µ –¥–ª—è —Ç–µ—Ö–Ω—ñ—á–Ω–æ–≥–æ –∑–±–µ—Ä—ñ–≥–∞–Ω–Ω—è –∫–æ–ª—å–æ—Ä—É (—è–∫ rgb —á–∏ hex).

// –§–æ—Ä–º–∞—Ç:
// oklch(L C H)
// L ‚Äî Lightness (—è—Å–∫—Ä–∞–≤—ñ—Å—Ç—å)
// C ‚Äî Chroma (–Ω–∞—Å–∏—á–µ–Ω—ñ—Å—Ç—å)
// H ‚Äî Hue (–≤—ñ–¥—Ç—ñ–Ω–æ–∫)

// ‚úÖ –ß–∏–º –≤—ñ–Ω –∫—Ä–∞—â–∏–π –∑–∞ RGB / HEX?
// –§–æ—Ä–º–∞—Ç	–ü—Ä–æ–±–ª–µ–º–∞ / –û—Å–æ–±–ª–∏–≤—ñ—Å—Ç—å

// rgb(255, 120, 0)	–õ—é–¥–∏–Ω—ñ –≤–∞–∂–∫–æ –∑—Ä–æ–∑—É–º—ñ—Ç–∏, –Ω–∞—Å–∫—ñ–ª—å–∫–∏ –≤—ñ–Ω —è—Å–∫—Ä–∞–≤–∏–π —á–∏ —Ç–µ–º–Ω–∏–π
// #FF7800	–©–µ –≥—ñ—Ä—à–µ ‚Äî –Ω–µ–º–∞ –∫–æ–Ω—Ç—Ä–æ–ª—é –Ω–∞–¥ —è—Å–∫—Ä–∞–≤—ñ—Å—Ç—é –∞–±–æ –∫–æ–Ω—Ç—Ä–∞—Å—Ç–æ–º
// oklch(0.7 0.15 40)	–ß—ñ—Ç–∫–æ –≤–∏–¥–Ω–æ: 70% —Å–≤—ñ—Ç–ª–æ—Å—Ç—ñ, 15 –Ω–∞—Å–∏—á–µ–Ω–æ—Å—Ç—ñ, –≤—ñ–¥—Ç—ñ–Ω–æ–∫ 40¬∞

// üéØ –û—Å–Ω–æ–≤–Ω–∞ –ø–µ—Ä–µ–≤–∞–≥–∞ ‚Äî –∫–æ–Ω—Ç—Ä–∞—Å—Ç —ñ accessibility

// –ù–∞–ø—Ä–∏–∫–ª–∞–¥, —è–∫—â–æ —É —Ç–µ–±–µ —î text-primary —ñ bg-primary, —É RGB –¥—É–∂–µ –≤–∞–∂–∫–æ –∑–∞–±–µ–∑–ø–µ—á–∏—Ç–∏ –¥–æ—Å—Ç—É–ø–Ω–∏–π –∫–æ–Ω—Ç—Ä–∞—Å—Ç (WCAG).
// –í oklch —Ü–µ –º–æ–∂–Ω–∞ –ø—Ä–æ—Å—Ç–æ —Ä–µ–≥—É–ª—é–≤–∞—Ç–∏ —Ä—ñ–≤–µ–Ω—å Lightness (L) ‚Üí –∫–æ–ª—å–æ—Ä–∏ –∑–∞–ª–∏—à–∞—é—Ç—å—Å—è "–æ–¥–Ω–æ–≥–æ —Å—Ç–∏–ª—é",–∞–ª–µ –∫–æ–Ω—Ç—Ä–∞—Å—Ç –∑–±–µ—Ä—ñ–≥–∞—î—Ç—å—Å—è.
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}
