import '../globals.css';
import type { Metadata } from 'next';
import { NextIntlClientProvider, hasLocale } from 'next-intl';
import { setRequestLocale } from 'next-intl/server';
import { notFound } from 'next/navigation';
import { routing } from '@/i18n/routing';
import { ThemeProvider } from '@/components/ThemeProvider';
import { BackgroundContainer } from '@/components/BackgroundContainer';
import { Geist, Geist_Mono } from 'next/font/google';
import { AnimatePresence } from 'framer-motion';
import { Header } from '@/components/Header';
import { LanguageSwitcher } from '@/components/LanguageSwitcher';
import { Lamp } from '@/components/Lamp';
import { cn } from '@/lib/utils';
import styles from './layout.module.css';
import { Configurator } from '@/components/Configurator';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

// https://next-intl.dev/docs/routing/setup#static-rendering
// https://nextjs.org/docs/app/api-reference/functions/generate-static-params#static-rendering
export function generateStaticParams() {
  return routing.locales.map((locale) => ({ locale }));
}

type Props = {
  children: React.ReactNode;
  params: Promise<{ locale: string }>;
};

export default async function LocaleLayout({ children, params }: Props) {
  const { locale } = await params;

  if (!hasLocale(routing.locales, locale)) {
    notFound();
  }

  // Enable static rendering
  setRequestLocale(locale);

  return (
    <html lang={locale} dir="ltr" suppressHydrationWarning>
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased`}>
        <NextIntlClientProvider>
          <ThemeProvider attribute="class" defaultTheme="light">
            <div className={styles.mainContainer}>
              <div className={`${styles.mainContent} ${styles.mainContentFullHeight}`}>
                <Configurator />
                <Lamp />
                <LanguageSwitcher />
                <Header />
                <div className={cn('w-full max-w-[1200px] mx-auto px-4')}>
                  <AnimatePresence mode="wait">{children}</AnimatePresence>
                </div>
              </div>
            </div>
          </ThemeProvider>
          <BackgroundContainer />
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
